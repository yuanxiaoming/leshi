
package com.ch.leyu.responseparse;

/** 视频库返回的title以及查询索引 */
public class TagResponse {
    String tag;

    String keyword;

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getKeyword() {
        return keyword;
    }

    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }
}
